// Exercício 6: Escreva um algoritmo para ler as dimensões de um retângulo (base e altura), calcular e escrever a área do retângulo. 

alert("Calculo da área de um triângulo");

let base = parseFloat(prompt("Informe o tamanho da base do triângulo."));
let altura = parseFloat(prompt("Informe a altura do triângulo"));
let area = (altura * base / 2);

alert("A área do triângulo é: " + area)