// Exercício 2: O usuário vai me informar valores, e será efetuado sua subtração.

alert("Subtração");
let valor1 = parseFloat(prompt ("Informe o primeiro valor para efetuar a subtração: "));
let valor2 = parseFloat(prompt("Informe o segundo valor para efetuar a subtração: "));

let subtracao = (valor1 - valor2);

alert("O resultado da subtração dos valores informados é: " + subtracao);